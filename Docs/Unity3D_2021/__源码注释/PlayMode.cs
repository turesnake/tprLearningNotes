namespace UnityEngine
{
    //
    // 摘要:
    //     Used by Animation.Play function.
    public enum PlayMode
    {
        //
        // 摘要:
        //     Will stop all animations that were started in the same layer. This is the default
        //     when playing animations.
        StopSameLayer = 0,
        //
        // 摘要:
        //     Will stop all animations that were started with this component before playing.
        StopAll = 4
    }
}

